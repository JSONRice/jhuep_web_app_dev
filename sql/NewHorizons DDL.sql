CREATE TABLE PLANETARY_ENTITY (
    PLANETARY_ENTITY_ID MEDIUMINT NOT NULL AUTO_INCREMENT,
    ISPLANET BIT(0),
    ISMOON BIT(0),
    NAME CHAR(64),
    ORBITING_MOON_SURVEY CHAR(200),
