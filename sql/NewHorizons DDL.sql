--drop table NH_USERS;

create table NH_USERS (
    USER_ID MEDIUMINT NOT NULL AUTO_INCREMENT,
    USERNAME CHAR(200),
    FIRSTNAME CHAR(200),
    LASTNAME CHAR(200),
    ITEMS BLOB,
    EMAIL CHAR(200),
    PASSWORD CHAR(200),
    PRIMARY KEY (USER_ID)
)

